package Semana1.Martes;

public class Sum {
  public static void main (String[] args) {
    int n = 5;
    double a = 4.56;
    byte c = 'a';
    double sum = n + a;
    double sub = a - n;
    System.out.println("Value of sum = " + sum);
    System.out.println("Value of sub =" + sub);
    System.out.printf("Value of %c is %d", c, c);
  }
}